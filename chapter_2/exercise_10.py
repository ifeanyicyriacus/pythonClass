num1 = int(input("Enter the first integer: "))
num2 = int(input("Enter the first integer: "))
num3 = int(input("Enter the first integer: "))
sum = num1 + num2 + num3
print("Their sum is: ", sum)
print("Their average is: ", sum / 3)
print("Their product is: ", num1 * num2 * num3)
print("The smallest is: ", min(num1, num2, num3))
print("Their largest is: ", max(num1, num2, num3))
