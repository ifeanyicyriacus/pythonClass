#exercise 2.12 (7% Investment return)

principal = 1000
rate = 7 / 100
ten_yrs = principal * (1 + rate) ** 10
twenty_yrs = principal * (1 + rate) ** 20
thirty_yrs = principal * (1 + rate) ** 30

print("After 10 yrs the ROI is: ", ten_yrs)
print("After 20 yrs the ROI is: ", twenty_yrs)
print("After 30 yrs the ROI is: ", thirty_yrs)
